int	ft_toupper(int c)
{
	if (c >= 'a' && c <= 'z')
	c = c - 32;
return (c);
}

#include<stdio.h>
int main (void)
{
	int	c;

	c = 'e';
	
	printf("%d\n", ft_toupper(c));
}